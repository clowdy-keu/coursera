'''Задача со звездочкой.
Дано четырехзначное число.
Определите, является ли его десятичная запись симметричной.
Если число симметричное, то выведите 1,
иначе выведите любое другое целое число.
Число может иметь меньше четырех знаков,
тогда нужно считать, что его десятичная запись дополняется слева нулями.

Формат ввода

Вводится единственное число.

Формат вывода

Выведите ответ на задачу.

Примечание

Десятичная запись числа симметрична,
если при прочтении слева направо и
справа налево получается одно и то же число.
'''
number = int(input())
externalDigitsIsEqual = int(not(number % 10 - number // 1000))
internalDigitsIsEqual = int(not(number % 100 // 10 - number // 100 % 10))
print(externalDigitsIsEqual * internalDigitsIsEqual)
